Point drawIcon(Point p, int colour, int backgroundcolour, int Erase, int icon){
	const unsigned char* arr;
	register int i, row, column, theX = p.x, theY = p.y ;
	register unsigned int BitMask, pixels;
	register char theColour = colour;
	register char theBackColour = backgroundcolour;

	switch(icon){
	case BACKSPACE:
		arr = BackSpaceIcon;
		break;
	case UPARROW:
		arr = UpperCaseIcon;
		break;
	case CAMERA:
		arr = CameraIcon;
		break;
	case HEART:
		arr = HeartIcon;
		break;
	case APPICON:
		arr = AppIcon;
		break;
	case BIOMED:
		arr = BioMedSensorIcon;
		break;
	default:
		arr = BackSpaceIcon;
		break;
	}

    if(((short)(p.x) > (short)(XRES-1)) || ((short)(p.y) > (short)(YRES-1)))
        return p;

	for(row = 0; row < ICONRES ; row++)	{
		for(i=0; i<2; i++){
			pixels = ((uint64_t) 0xff000000 & (arr[row*NUMCULS + i*4] << 24)) | ((uint64_t)0x00ff0000 & (arr[row*NUMCULS + 1 + i*4] << 16)) |
					((uint64_t)0x0000ff00 & (arr[row*NUMCULS + 2 + i*4] << 8)) | ((uint64_t)0x000000ff & arr[row*NUMCULS + 3 + i*4]);

			BitMask = 0x80000000;
			for(column = 0; column < ICONRES/2; column++){
				if((pixels & BitMask))
					WriteAPixel((Point){theX+column + i*32, theY+row}, theColour) ;
				else if(Erase == 1)
					WriteAPixel((Point){theX+column + i*32, theY+row}, theBackColour) ;
				BitMask = BitMask >> 1 ;
			}
		}
	}

	return (Point){p.x + ICONRES, p.y + ICONRES};
}

